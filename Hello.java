package MyProgrammes;

public class Hello {

	public static void main(String[] args) {
		System.out.println("Hello World");
		
		//=========================================
		//=====Integral Data Types
		//=========================================
		
		byte b=120;    // Byte Range -128 to 127
		               // Byte size 1 byte
		
		System.out.println(b);
		
		b=-125;
		
	    short s=32767;     // Short range -32768 to 32767
	                       // Size 2 bytes
	    
	    System.out.println(s);
	    
	    int i=2147483647;   // Size 4 bytes
	    
	    System.out.println(i);
	    
	    
	    long l=9786547383L;    // Size 8 bytes
	    
	    System.out.println(l);
	    
	  //=========================================
	  //=====Floating Point Data Types
	  //=========================================

	    float f=3.12345678943f;
	    
	    System.out.println(f);
	    
	    double d=4.1234567891234577d;
	    
	    System.out.println(d);
	    
	   //========================================
	   //====Boolean Data Type===================
	   //==== Size 1 bit
	    
	    boolean bl=true;
	    
	    System.out.println(bl);
	    
	    //=======================================
	    //====Char Data Type=====================
	    
	    char ch='A';    // Size 2 bytes
	    
	    System.out.println(ch);
	    
	    
	}
}
